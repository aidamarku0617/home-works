package Class7;

public class E1NestedLoops {
    public static void main(String[] args) {
        for(int i=0; i<5; i++){
            System.out.print("*");
        }
        System.out.println(); //to make space in raw
        for (int i=0; i<5;i++){
            System.out.print("*");
        }
    }
}
